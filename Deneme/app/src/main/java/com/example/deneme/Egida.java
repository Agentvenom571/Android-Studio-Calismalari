package com.example.deneme;

public class Egida {

    private String gida;
    private String deger;

    public String getGida() {
        return gida;
    }

    public void setGida(String gida) {
        this.gida = gida;
    }

    public String getDeger() {
        return deger;
    }

    public void setDeger(String deger) {
        this.deger = deger;
    }

    public Egida(String gida, String deger) {
        this.gida = gida;
        this.deger = deger;
    }

}
